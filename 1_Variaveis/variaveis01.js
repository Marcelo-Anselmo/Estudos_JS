//Aqui esta uma das maneiras mais basicas de declarar uma variável

//Quando se trata de declarar váriaveis temos 3 possibilidades mais comuns, let|const|var, cada uma com seu propósito.

/*
    let: Imagine que você tem uma caixinha que pode mudar de cor e de conteúdo sempre que você quiser. Um dia você pode colocar seus carrinhos nela, no outro dia suas bonecas, e assim por diante. Com o "let", você pode mudar o que tem dentro da caixinha quando quiser.

    const: Agora, pense em outra caixinha que você só coloca algo uma vez e não pode mais trocar. Vamos dizer que você coloca sua pelúcia favorita nessa caixinha. Depois disso, não dá para tirar a pelúcia e colocar outro brinquedo no lugar. O "const" é como essa caixinha, você coloca algo nela e não pode mais mudar.

    var: Por último, imagine uma caixinha um pouco mais velha e mais bagunçada. Às vezes, essa caixinha pode ter problemas e trocar o que tem dentro dela sem você perceber. Pode ser confuso! No JavaScript, "var" é como essa caixinha mais velha, ela não é tão cuidadosa em manter as coisas organizadas e pode causar problemas se não for usada com cuidado.

*/
var A1 = "Prisma"
var A1 = "Corola"

